package com.huuthang.weather_json_api;

public class Weather {
    public String toado;
    public String nhetdo;
    public String apsuat;
    public String tocdogio;
    public String may;
    public String hinh;

    public String getToado() {
        return toado;
    }

    public void setToado(String toado) {
        this.toado = toado;
    }

    public String getNhetdo() {
        return nhetdo;
    }

    public void setNhetdo(String nhetdo) {
        this.nhetdo = nhetdo;
    }

    public String getApsuat() {
        return apsuat;
    }

    public void setApsuat(String apsuat) {
        this.apsuat = apsuat;
    }

    public String getTocdogio() {
        return tocdogio;
    }

    public void setTocdogio(String tocdogio) {
        this.tocdogio = tocdogio;
    }

    public String getMay() {
        return may;
    }

    public void setMay(String may) {
        this.may = may;
    }

    public String getHinh() {
        return hinh;
    }

    public void setHinh(String hinh) {
        this.hinh = hinh;
    }
}
